package com.manorrock.bengal.message.core;

/**
 * A Bengal Message - 'method'
 * 
 * @author Manfred Riem (mriem@manorrock.com)
 */
public class MessageMethod {
    
    /**
     * Stores the name.
     */
    private String name;
    
    /**
     * Get the name.
     * 
     * @return the name.
     */
    public String getName() {
        return name;
    }
    
    /**
     * Set the name.
     * 
     * @param name the name.
     */
    public void setName(String name) {
        this.name = name;
    }
}
